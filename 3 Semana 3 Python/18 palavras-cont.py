frase = input("Digite uma frase qualquer: ")

palavras = []
index = 0

for i in range(0, len(frase)):
    if frase[i] == ' ':
        palavras.append(frase[index:i])
        index = i

print(f"A quantidade de palavras na frase Ã©: {len(palavras)}")